//----------------------------------------------------------------------------
//----------------------------------------------------------------------------
static double ecg_anomaly_function(event_data const &event)
{
  double LO = 5.25, HI = 6.0;
  int LO_RANGE = 0;

  if(rand() % 2) LO_RANGE = 1;

  if(LO_RANGE)
  {
    LO = 3.0;
    HI = 4.5;
  }

  double data = 
    (HI-LO)*(static_cast<double>(rand())/static_cast<double>(RAND_MAX)) + LO;

  return data;
}


